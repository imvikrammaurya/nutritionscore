'use client';
import { useEffect, useState } from 'react';
import { useRouter } from 'next/navigation';

export default function ResultPage() {
    const [r, setR] = useState<any>(null);
    const router = useRouter();
    useEffect(() => {
        const d = localStorage.getItem('ns_result');
        if (d) setR(JSON.parse(d)); else router.push('/');
    }, []);
    if (!r) return <div className="min-h-screen flex items-center justify-center"><p>Loading...</p></div>;
    const sc = r.health_score;
    const col = sc >= 7 ? '#15803d' : sc >= 4 ? '#b45309' : '#b91c1c';
    const bg = sc >= 7 ? '#f0fdf4' : sc >= 4 ? '#fffbeb' : '#fef2f2';
    return (
        <main className="min-h-screen bg-gray-50 p-5">
            <div className="max-w-xl mx-auto">
                <button onClick={() => router.push('/')} className="text-indigo-700 text-sm mb-5">
                    &#8592; Scan another product
                </button>
                <div className="rounded-2xl p-6 mb-4 text-center" style={{ background: bg }}>
                    <p className="text-sm text-gray-500">NutritionScore</p>
                    <p className="text-7xl font-bold my-1" style={{ color: col }}>{sc}</p>
                    <p style={{ color: col }} className="text-lg font-medium">/10 &mdash; {r.verdict}</p>
                    <p className="text-sm text-gray-600 mt-2">{r.explanation}</p>
                    {r.product_name && <p className="text-xs text-gray-400 mt-1">Product: {r.product_name}</p>}
                </div>
                {r.good_aspects?.length > 0 && (
                    <div className="bg-white rounded-2xl p-4 mb-3 shadow-sm">
                        <h2 className="font-semibold text-green-700 mb-2 text-sm">What is good</h2>
                        {r.good_aspects.map((g: string, i: number) => (
                            <span key={i} className="inline-block bg-green-100 text-green-800 text-xs px-2 py-1 rounded-full mr-2 mb-1">{g}</span>
                        ))}
                    </div>
                )}
                {r.bad_ingredients?.length > 0 && (
                    <div className="bg-white rounded-2xl p-4 mb-3 shadow-sm">
                        <h2 className="font-semibold text-red-700 mb-2 text-sm">Problem Ingredients</h2>
                        {r.bad_ingredients.map((it: any, i: number) => (
                            <div key={i} className="border-b border-gray-100 pb-2 mb-2 last:border-0">
                                <div className="flex justify-between">
                                    <p className="font-medium text-sm">{it.name}
                                        <span className="text-gray-400 text-xs ml-1">({it.amount})</span>
                                    </p>
                                    <span className={it.severity === 'high' ? 'text-red-500 text-xs' : 'text-amber-500 text-xs'}>
                                        {it.severity}
                                    </span>
                                </div>
                                <p className="text-xs text-gray-500">{it.reason}</p>
                            </div>
                        ))}
                    </div>
                )}
                <div className="bg-white rounded-2xl p-4 mb-3 shadow-sm">
                    <h2 className="font-semibold text-indigo-700 mb-1 text-sm">Healthier Alternatives</h2>
                    <p className="text-xs text-gray-400 mb-3">
                        {r.alternatives_source === 'alloydb_cache'
                            ? 'Instant result from AlloyDB similarity search'
                            : 'AI-generated by AlternativeFinder and saved to AlloyDB'}
                    </p>
                    {r.alternatives?.map((a: any, i: number) => (
                        <div key={i} className="border border-indigo-100 rounded-xl p-3 mb-2 bg-indigo-50">
                            <div className="flex justify-between items-start">
                                <div>
                                    <p className="font-medium text-sm">{a.name}</p>
                                    <p className="text-xs text-gray-500">{a.brand}</p>
                                </div>
                                <span className="text-green-700 font-bold text-sm">{a.estimated_score}/10</span>
                            </div>
                            <p className="text-xs text-gray-600 mt-1">{a.why_better}</p>
                            <p className="text-xs text-indigo-600 mt-1">Buy at: {a.where_to_buy}</p>
                        </div>
                    ))}
                </div>
                {r.agent_log?.length > 0 && (
                    <div className="bg-gray-900 rounded-2xl p-4">
                        <p className="text-gray-500 text-xs mb-2 font-mono">AGENT ACTIVITY LOG</p>
                        {r.agent_log.map((l: string, i: number) => (
                            <p key={i} className="text-green-400 text-xs font-mono leading-5">&#9658; {l}</p>
                        ))}
                    </div>
                )}
            </div>
        </main>
    );
}